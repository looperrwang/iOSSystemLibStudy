#ifndef LearningFootprints_h
#define LearningFootprints_h

struct vec3
{
    float x, y, z;
    
    vec3() : x(0), y(0), z(0) {}
    
    vec3(float a, float b, float c)
    {
        x = a;
        y = b;
        z = c;
    }
};


#endif /* LearningFootprints_h */
